(async ()=>{
  const ORG = 'kidwords';
  const container = document.getElementById('repos');
  const CACHE_KEY = 'navigator_repos_v1';
  const CACHE_TTL = 1000 * 60 * 5; // 5 minutes

  function el(tag, cls, txt){ const e = document.createElement(tag); if(cls) e.className = cls; if(txt) e.textContent = txt; return e; }

  function saveCache(data){ try{ localStorage.setItem(CACHE_KEY, JSON.stringify({ts:Date.now(),data})); }catch(e){} }
  function loadCache(){ try{ const s = localStorage.getItem(CACHE_KEY); if(!s) return null; const obj = JSON.parse(s); if(Date.now() - obj.ts > CACHE_TTL) return null; return obj.data; }catch(e){ return null; } }

  async function fetchAllRepos(){
    // If user selected a proxy mode, prefer using proxy endpoint
    const proxyMode = localStorage.getItem('navigator:proxyMode') || 'auto';
    const proxyUrl = (localStorage.getItem('navigator:proxyUrl') || '').trim();

    if(proxyMode === 'worker' || proxyMode === 'custom'){
      const base = proxyUrl || '';
      if(base){
        const sep = base.includes('?') ? '&' : '?';
        const url = `${base}${sep}org=${ORG}`;
        const headers = {};
        const apiKey = localStorage.getItem('navigator:proxyApiKey') || '';
        if(apiKey){ headers['x-api-key'] = apiKey; }
        const r = await fetch(url, {cache: 'no-cache', headers});
        if(!r.ok) throw new Error('Proxy error '+r.status);
        const arr = await r.json();
        return arr;
      }
      // if worker selected but no URL configured, fallthrough to auto
    }

    if(proxyMode === 'api'){
      // force calling API (no static JSON)
      return fetchFromApi();
    }

    // default 'auto': try static JSON generated by Actions to avoid API rate limits
    try{
      const jsonUrl = '/navigator/data/repos.json';
      const r = await fetch(jsonUrl, {cache: 'no-cache'});
      if(r.ok){
        const arr = await r.json();
        if(Array.isArray(arr) && arr.length>0) return arr;
      }
    }catch(e){ /* ignore and fallback to API */ }

    return fetchFromApi();
  }

  async function fetchFromApi(){
    const per = 100; let page = 1; let out = [];
    // Try org first, fallback to user
    let urlBase = `https://api.github.com/orgs/${ORG}/repos`;
    let triedOrg = true;

    while(true){
      const url = `${urlBase}?per_page=${per}&page=${page}`;
      const res = await fetch(url);
      if(res.status === 404 && triedOrg){ // maybe it's a user account
        urlBase = `https://api.github.com/users/${ORG}/repos`;
        triedOrg = false; page = 1; continue;
      }
      if(!res.ok) throw new Error('HTTP '+res.status);
      const arr = await res.json();
      if(!Array.isArray(arr)) break;
      out = out.concat(arr);
      if(arr.length < per) break;
      page++;
    }

    // sort by pushed_at desc
    out.sort((a,b)=> new Date(b.pushed_at) - new Date(a.pushed_at));
    return out;
  }

  function renderEmpty(msg){ container.innerHTML = ''; const p = el('p', null, msg); container.appendChild(p); }

  function makeCard(repo){
    const card = el('article','card');
    // set dataset for filtering
    card.dataset.name = repo.name || '';
    card.dataset.desc = repo.description || '';
    card.appendChild(el('h3',null, repo.full_name));
    card.appendChild(el('div','meta', repo.description || '—'));
    const stats = el('div','stats', `⭐ ${repo.stargazers_count} · 🍴 ${repo.forks_count} · issues ${repo.open_issues_count}`);
    card.appendChild(stats);

    // badges (shields.io) container
    const badges = el('div','badges');
    // stars badge
    const starsImg = document.createElement('img');
    starsImg.src = `https://img.shields.io/github/stars/${repo.owner.login}/${repo.name}.svg?style=flat-square&label=Stars`;
    starsImg.alt = 'stars';
    badges.appendChild(starsImg);
    // forks badge
    const forksImg = document.createElement('img');
    forksImg.src = `https://img.shields.io/github/forks/${repo.owner.login}/${repo.name}.svg?style=flat-square&label=Forks`;
    forksImg.alt = 'forks';
    badges.appendChild(forksImg);
    card.appendChild(badges);

    const pushed = repo.pushed_at ? new Date(repo.pushed_at).toLocaleString() : '—';
    card.appendChild(el('div',null, `最近推送: ${pushed}`));

    const btnRow = el('div', null);
    const repoLink = document.createElement('a'); repoLink.className='btn'; repoLink.href = repo.html_url; repoLink.target='_blank'; repoLink.rel='noopener'; repoLink.textContent = '打开 仓库';
    btnRow.appendChild(repoLink);

    const pagesBtn = document.createElement('a'); pagesBtn.className='btn'; pagesBtn.style.marginLeft = '8px';
    pagesBtn.target = '_blank'; pagesBtn.rel = 'noopener';
    if(repo.has_pages){
      pagesBtn.href = `https://${repo.owner.login}.github.io/${repo.name}/`;
      pagesBtn.textContent = '打开 Pages';
    }else{
      pagesBtn.href = '#'; pagesBtn.textContent = '无 Pages'; pagesBtn.style.opacity = '0.6'; pagesBtn.style.pointerEvents = 'none';
    }
    btnRow.appendChild(pagesBtn);

    card.appendChild(btnRow);

    // asynchronously try to fetch workflows to show actions badge
    (async ()=>{
      try{
        const wres = await fetch(`https://api.github.com/repos/${repo.owner.login}/${repo.name}/actions/workflows`);
        if(wres.ok){
          const wj = await wres.json();
          if(wj.workflows && wj.workflows.length>0){
            const wf = wj.workflows[0];
            // workflow path like .github/workflows/ci.yml -> encode
            const wfPath = encodeURIComponent(wf.path || wf.name || wf.id);
            const actionsImg = document.createElement('img');
            actionsImg.src = `https://github.com/${repo.owner.login}/${repo.name}/actions/workflows/${wfPath}/badge.svg`;
            actionsImg.alt = 'actions status';
            actionsImg.title = wf.name || wf.path || 'workflow';
            badges.appendChild(actionsImg);
          }
        }
      }catch(e){ /* ignore */ }
    })();

    return card;
  }

  // pagination + sorting state
  let allRepos = [];
  let filteredRepos = [];
  let currentPage = 1;
  let perPage = 12;
  let currentSort = 'pushed';

  function compareRepos(a,b, sort){
    if(sort === 'stars') return (b.stargazers_count||0) - (a.stargazers_count||0);
    if(sort === 'name') return (a.name||'').localeCompare(b.name||'');
    // default pushed
    return new Date(b.pushed_at || 0) - new Date(a.pushed_at || 0);
  }

  function renderPage(page=1){
    currentPage = page;
    container.innerHTML = '';
    const start = (page-1)*perPage; const end = start + perPage;
    const pageItems = filteredRepos.slice(start,end);
    if(pageItems.length === 0){ renderEmpty('没有符合条件的仓库'); return; }
    pageItems.forEach(r => container.appendChild(makeCard(r)));
    // pager
    const pager = document.getElementById('pager');
    pager.innerHTML = '';
    const totalPages = Math.max(1, Math.ceil(filteredRepos.length / perPage));
    const prev = document.createElement('button'); prev.textContent = '‹ 上一页';
    prev.disabled = page <= 1; prev.onclick = ()=> renderPage(page-1);
    pager.appendChild(prev);
    // show up to 7 page buttons centered on current
    const maxButtons = 7; let startPage = Math.max(1, page - Math.floor(maxButtons/2));
    let endPage = Math.min(totalPages, startPage + maxButtons -1);
    if(endPage - startPage < maxButtons -1) startPage = Math.max(1, endPage - maxButtons +1);
    for(let p = startPage; p<=endPage; p++){
      const b = document.createElement('button'); b.textContent = p; if(p===page) b.className='active'; b.onclick = ()=> renderPage(p); pager.appendChild(b);
    }
    const next = document.createElement('button'); next.textContent = '下一页 ›'; next.disabled = page >= totalPages; next.onclick = ()=> renderPage(page+1); pager.appendChild(next);
  }

  async function loadAndRender(force=false){
    container.innerHTML = '';
    const cached = loadCache();
    if(cached && !force){
      allRepos = cached;
      filteredRepos = allRepos.filter(r=>!r.fork);
      filteredRepos.sort((a,b)=> compareRepos(a,b,currentSort));
      saveCache(filteredRepos);
      renderPage(1);
      const info = el('div','footer', `（从缓存读取 ${filteredRepos.length} 个仓库）`);
      container.appendChild(info);
      return;
    }

    const loading = el('div',null,'加载仓库列表...'); container.appendChild(loading);
    try{
      const repos = await fetchAllRepos();
      allRepos = repos;
      container.innerHTML = '';
      if(!repos || repos.length === 0){ renderEmpty('未找到仓库'); return; }
      // Optionally filter: remove forks
      filteredRepos = repos.filter(r=>!r.fork);
      filteredRepos.sort((a,b)=> compareRepos(a,b,currentSort));
      saveCache(filteredRepos);
      renderPage(1);
      const footer = el('div','footer', `共 ${filteredRepos.length} 个仓库（forks 已过滤）。`);
      document.getElementById('pager').after(footer);
    }catch(err){ renderEmpty('获取失败: '+err.message); }
  }

  // add refresh button and wire search input
  const hdr = document.querySelector('.container');

  const searchEl = document.getElementById('search');
  const refreshBtn = document.getElementById('refresh');
  const sortEl = document.getElementById('sort');
  const perPageEl = document.getElementById('perPage');
  const proxyModeEl = document.getElementById('proxyMode');
  const proxyUrlEl = document.getElementById('proxyUrl');
  const saveProxyBtn = document.getElementById('saveProxy');
  const proxyApiKeyEl = document.getElementById('proxyApiKey');

  // restore proxy settings
  const storedMode = localStorage.getItem('navigator:proxyMode') || 'auto';
  const storedUrl = localStorage.getItem('navigator:proxyUrl') || '';
  const storedKey = localStorage.getItem('navigator:proxyApiKey') || '';
  if(proxyModeEl) proxyModeEl.value = storedMode;
  if(proxyUrlEl) proxyUrlEl.value = storedUrl;
  if(proxyApiKeyEl) proxyApiKeyEl.value = storedKey;

  if(saveProxyBtn) saveProxyBtn.onclick = ()=>{
    const m = proxyModeEl.value; const u = proxyUrlEl.value.trim(); const k = proxyApiKeyEl.value.trim();
    localStorage.setItem('navigator:proxyMode', m);
    localStorage.setItem('navigator:proxyUrl', u);
    localStorage.setItem('navigator:proxyApiKey', k);
    alert('数据源配置已保存，正在刷新');
    saveCache([]); loadAndRender(true);
  };

  if(refreshBtn) refreshBtn.onclick = ()=>{ saveCache([]); loadAndRender(true); };

  function applyFilter(q){
    q = (q||'').trim().toLowerCase();
    // when filtering, operate on filteredRepos then re-render page
    if(!filteredRepos) return;
    const prevPage = currentPage;
    const visible = allRepos.filter(r=>{
      const name = (r.name||'').toLowerCase();
      const desc = (r.description||'').toLowerCase();
      return q === '' || name.includes(q) || desc.includes(q);
    });
    filteredRepos = visible.sort((a,b)=> compareRepos(a,b,currentSort));
    currentPage = 1;
    renderPage(1);
  }

  searchEl && searchEl.addEventListener('input', (e)=> applyFilter(e.target.value));
  searchEl && searchEl.addEventListener('keydown', (e)=>{ if(e.key === 'Enter') { if(!e.target.value) applyFilter(''); }});

  sortEl && sortEl.addEventListener('change', (e)=>{ currentSort = e.target.value; filteredRepos.sort((a,b)=> compareRepos(a,b,currentSort)); renderPage(1); });
  perPageEl && perPageEl.addEventListener('change', (e)=>{ perPage = parseInt(e.target.value,10) || 12; renderPage(1); });

  // initial load
  await loadAndRender();

  // apply empty filter to ensure data-attrs are used
  applyFilter('');

})();
